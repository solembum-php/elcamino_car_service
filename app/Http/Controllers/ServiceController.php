    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(Service $services)
    {
        $this->authorize('destroy', $services);
	$services->delete();
	return redirect(route('services.index'));
    }
